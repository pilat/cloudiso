// TestByteMatchSmoke runs the byte-match harness end-to-end for a single
// inline case and logs the diff if the bytes diverge. The test does NOT fail
// on mismatch — it exists to verify the harness itself runs without errors.
// A later convergence test will require byte-equality.
func TestByteMatchSmoke(t *testing.T) {
	skipIfNoDocker(t)

	fixed := time.Date(2026, 4, 18, 12, 0, 0, 0, time.UTC)
	c := &bytematchCase{
		Name:         "smoke_nocloud",
		VolumeID:     "cidata",
		Publisher:    "cloudiso",
		Preparer:     "cloudiso",
		AppID:        "CLOUDISO",
		CreationTime: fixed,
		ModTime:      fixed,
		Entries: []treeEntry{
			{Path: "/", IsDir: true, Mtime: fixed},
			{Path: "meta-data", Data: []byte("instance-id: test-001\n"), Mtime: fixed},
			{Path: "user-data", Data: []byte("#cloud-config\n"), Mtime: fixed},
		},
	}

	inputHostDir := filepath.Join(sharedHostDir, fmt.Sprintf("smoke-%d", time.Now().UnixNano()))
	require.NoError(t, materializeInput(t, inputHostDir, c))
	inputContainer := sharedContainerDir + "/" + filepath.Base(inputHostDir)

	env := newTestEnv(t, c.VolumeID, c.Publisher, c.CreationTime)
	theirsContainerPath := env.runGenisoimagePinned(t, inputContainer, "smoke-theirs.iso", c)

	theirsHost := filepath.Join(sharedHostDir, filepath.Base(theirsContainerPath))
	theirs, err := os.ReadFile(theirsHost)
	require.NoError(t, err, "read theirs.iso from shared host dir")

	ours := buildOurISO(t, c)
	ours, theirs, err = normalizeBytematchComparison(ours, theirs)
	require.NoError(t, err, "normalize bytematch comparison")

	if err := diffISO(ours, theirs); err != nil {
		t.Logf("smoke diff (expected at this stage):\n%s", err.Error())
	}
}

// TestByteMatch runs every bytematchCase through the harness and requires
// byte-equality with genisoimage. Set BYTEMATCH_QUICK=1 to skip the
// structured diff reporter and compare sha256 only.
func TestByteMatch(t *testing.T) {
	skipIfNoDocker(t)

	require.NotEmpty(t, bytematchCases, "bytematch_cases_test.go did not populate bytematchCases")

	quick := bytematchQuickMode()

	for _, c := range bytematchCases {
		t.Run(c.Name, func(t *testing.T) {
			inputHostDir := filepath.Join(sharedHostDir, fmt.Sprintf("bm-%s-%d", c.Name, time.Now().UnixNano()))
			require.NoError(t, materializeInput(t, inputHostDir, &c))
			inputContainer := sharedContainerDir + "/" + filepath.Base(inputHostDir)

			env := newTestEnv(t, c.VolumeID, c.Publisher, c.CreationTime)
			theirsContainerPath := env.runGenisoimagePinned(t, inputContainer,
				fmt.Sprintf("bm-%s.iso", c.Name), &c)

			theirs, err := os.ReadFile(filepath.Join(sharedHostDir, filepath.Base(theirsContainerPath)))
			require.NoError(t, err, "read theirs.iso")

			ours := buildOurISO(t, &c)
			ours, theirs, err = normalizeBytematchComparison(ours, theirs)
			require.NoError(t, err, "normalize bytematch comparison")

			var diff error
			if quick {
				diff = diffISOSummary(ours, theirs)
			} else {
				diff = diffISO(ours, theirs)
			}
			if diff != nil {
				t.Error(diff)
			}
		})
	}
}

