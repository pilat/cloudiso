// bytematchCase describes one corner case for the byte-match suite.
// All fields must be self-contained: no filesystem reads, no time.Now, no randomness.
type bytematchCase struct {
	Name         string
	VolumeID     string
	Publisher    string
	Preparer     string
	AppID        string
	CreationTime time.Time
	ModTime      time.Time
	Entries      []treeEntry
}

// treeEntry describes one node in the input tree.
// Path is POSIX-separated. Non-root entries have no leading slash.
// The single root entry uses the literal "/".
type treeEntry struct {
	Path  string // "/" for root; no leading slash for all others
	IsDir bool
	Data  []byte // nil for dirs
	Mtime time.Time
}

