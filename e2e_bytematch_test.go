// lookupPVDField returns the field name for a given byte offset within the PVD.
// It returns the name of the field whose start offset is the largest value
// less than or equal to off.
func lookupPVDField(off int) string {
	return lookupField(pvdFields, off)
}

// lookupSVDField returns the field name for a given byte offset within the SVD.
func lookupSVDField(off int) string {
	return lookupField(svdFields, off)
}

// lookupField performs a linear scan over a sorted []pvdField to find the
// field whose start offset is the largest value <= off.
func lookupField(fields []pvdField, off int) string {
	name := "unknown"
	for _, f := range fields {
		if f.offset <= off {
			name = f.name
		} else {
			break
		}
	}
	return name
}
